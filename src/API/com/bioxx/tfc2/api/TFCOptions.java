	public static int maxThreadsForIslandGen = 2;

	public static boolean getBooleanFor(Configuration config,String heading, String item, boolean value)
	{
		if (config == null)
			return value;
		try
		{
			Property prop = config.get(heading, item, value);
			return prop.getBoolean(value);
		}
		catch (Exception e)
		{
			System.out.println(new StringBuilder().append("[TFC2] Error while trying to add Integer, config wasn't loaded properly!").toString());
		}
		return value;
	}

